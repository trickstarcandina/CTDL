#include <bits/stdc++.h>
using namespace std;
int m;
long long n;
int a[100000];
void nhap(){
	cin>>m;
	a[0]=0; a[1]=1;
	int p=1; n=2;	
	for(int i=2;i<=m;i++){
		p=p*2;
		int u=2*n-1;
		for(int j=n;j<=u;j++) 
            a[j]=a[u-j]+p;
		n=n*2;
	}	
}
void xuat(){
	int b[20];
	for(int i=0;i<n;i++){
		int k=0,l;
		int u=a[i];
		while(u>0){
            l=k++;
			b[l]=u%2;  
			u/=2;			
		}
		for(int j=k+1;j<=m;j++) cout<<"0";
		while(k>=1) {
            l=--k;
            cout<<b[l];
        }
		cout<<" ";
	}	
}
int main(){
    int t; cin>>t;
    while(t--){
        nhap();
        xuat();
        cout<<endl;
    }
	return 0;
}
