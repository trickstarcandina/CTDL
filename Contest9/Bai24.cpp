#include <bits/stdc++.h> 
using namespace std; 
int v,e;
vector<int> a[1001];
int visit[1001];
bool check;
void nhap(){
    for(int i=0;i<1001;i++) 
        a[i].clear();
	//memset(ok,true,sizeof(ok));
    cin >> v >> e;
	int x , y;
	for(int i = 0 ; i < e ; i++){
		cin >> x >> y;
		a[x].push_back(y);
        a[y].push_back(x);
	}
}
void BFS(int u) { 
    queue<int> q;
    q.push(u);
    visit[u]=1;
    while(q.size()>0){
        if(check) break;
        int top=q.front();
        q.pop();
        for(int i=0;i<a[top].size();i++){
            int y=a[top][i];
            if(visit[y]==0){
                q.push(y);
                visit[y]=1;
            }
            else if(visit[y]==1) check=true;
        }
        visit[top]=2;
    }

}
void chutrinh(){
    check=false;
    for(int i=1;i<=v;i++){    
        BFS(i);
        if(check){
            cout<<"YES"; return;
        }
    }
    cout<<"NO";
}
int main(){
	int t; cin >> t;
	while(t--){
	    nhap();
	    chutrinh();
        cout<<endl;
	}
    return 0;
}
