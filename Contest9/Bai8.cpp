#include <bits/stdc++.h> 
using namespace std; 
int v,e,u;
int a[1001][1001];
int vs[1001];
int q[100];
void nhap(){
	cin >> v >> e >>u;
	memset(a,0,sizeof(a));
	memset(vs,0,sizeof(vs));
	int x , y;
	for(int i = 1 ; i <= e ; i++){
		cin >> x >> y;
		a[x][y] = 1;
		a[y][x] = 1;
	}
}

void Bfs(int u){ 
	int x, dq = 1, cq = 0; 
	cq++; q[cq] = u; 
	vs[u] = 1;
    while (dq <= cq){ 
		x = q[dq]; dq++; 
        cout << x << " ";
        for (int i= 1; i<=v; i++)
            if (vs[i]==0 && a[x][i]==1) {
                cq++; 
				q[cq] = i; 
				vs[i] = 1; 	
			}
    }
}

int main(){
	int t; cin >> t;
	while(t--){
	   nhap();
	   Bfs(u);
	   cout<<endl;
	}
    return 0;
}
