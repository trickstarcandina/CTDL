#include<bits/stdc++.h>
using namespace std;
int V , E , nguon , dich;
int a[1001][1001]; 
bool vs[1001] ,check;
int e[1001];
void nhap(){
	check = false;
	cin >> V >> E >> nguon >> dich;
	memset(a,0,sizeof(a));
    for(int i = 1 ; i <= V ; i++) vs[i] = true;	
	int x,y;
	for(int i = 1 ; i <= E ; i++){
		cin >> x >> y;
		a[x][y] = 1;
        a[y][x] = 1; 
	}
}
void xuly(int nguon , int k){
	if(check) 
        return;
	e[k] = nguon;
	vs[nguon] = false;
	if(nguon == dich) {
		check = true;
		for(int i = 1 ; i <= k ; i++) 
            cout << e[i] << " ";
		return;
	}
	for(int i = 1 ; i <= V ; i++){
		if(vs[i] && a[nguon][i] == 1) 
            xuly(i,k+1);
	}
}
int main(){
	int T;
	cin >> T;
	while(T--){
	    nhap();
	    xuly(nguon,1);
	    if(check == false) 
            cout << "-1";
	    cout << endl;
	}
}
