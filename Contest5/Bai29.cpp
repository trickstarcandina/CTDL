#include <bits/stdc++.h> 
using namespace std; 
int c[41];  
int xuly(string str){ 
  int n=str.length();
	c[0] = 1; 
	c[1] = 1; 
  //nếu ký tự lúc đầu ==0
	if(str[0]=='0') 
		return 0;
	for (int i = 2; i <= n; i++) { 
		c[i] = 0; 
		//nếu số cuối cùng khác 0, thì chữ số cuối cùng phải thêm vào số lượng từ
		if (str[i-1] > '0') 
			c[i] = c[i-1]; 
		// nếu số cuối = 7 và số trước nó =2 thì loại 
		if (str[i-2] == '1' || (str[i-2] == '2' && str[i-1] < '7') ) 
			c[i] += c[i-2]; 
	} 
	return c[n]; 
} 
int main() { 
	int t; cin>>t;
	while (t--){
        string str; cin>>str;
        cout << xuly(str); 
        cout<<endl;
	}
	return 0; 
} 
