#include <bits/stdc++.h> 
using namespace std; 
int a[101];
int tang[101],giam[101];
void nhap(int a[],int n){
    for(int i=0;i<n;i++)
        cin>>a[i];
}
int xuly(int a[], int n) { 
	int result = -1; 
	// tang[i] tinh max day tang 
	// giam[i] tinh max day giam 
	for (int i = 0; i < n; i++) { 
		tang[i] = a[i]; 
		giam[i] = a[i]; 
	} 
	// tang tu trai sang phai
	for (int i = 1; i < n; i++) 
		for (int j = 0; j < i; j++){
            if (a[i] > a[j] && tang[i] < tang[j] + a[i]) 
				tang[i] = tang[j] + a[i];
        } 		 
	// giam ngc lai 
	for (int i = n - 2; i >= 0; i--) 
		for (int j = n - 1; j > i; j--){
            if (a[i] > a[j] && giam[i] < giam[j] + a[i]) 
				giam[i] = giam[j] + a[i];
        } 
	for (int i = 0; i < n; i++) 
		result = max(result, (tang[i] + giam[i] - a[i])); 
	return result; 
} 
int main() { 
	int t; cin>>t;
	while (t--){
        int n; cin>>n;
        nhap(a,n);
        cout<<xuly(a, n);
        cout<<endl;
	}
	return 0; 
} 
