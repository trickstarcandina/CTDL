long long xuly(int n) { 
    long long x1 = a[0]; 
    long long x2 = 0; 
    long long result; 
    for (int i = 1; i < n; i++) { 
        if(x1>x2) result=x1;
        else result=x2; 
        x1 = x2 + a[i]; 
        x2 = result; 
    } 
    if(x1>x2) return x1;
    else return x2; 
}  
