#include<bits/stdc++.h> 
using namespace std;  
long long c[105];
long long xuly(int n, int ins,int del,int cpy) { 
    memset(c,0,sizeof(c));
    c[0]=0; c[1]=ins;
    for(int i=1;i<=n;i++){
        //chẵn thì tìm min từ kq cho (i-1) ký tự và thời gian để ins 
        //và kq cho một nửa số ký tự và thời gian để cpy
        if (i % 2 == 0) 
            c[i] = min(c[i-1] + ins, c[i/2] + cpy);
        //lẻ thì tìm min từ kq cho (i-1) ký tự và thời gian cho ins 
        //và kq cho một nửa số ký tự và thời gian cho cpy và del 1 ký tự 
        else
            c[i] = min(c[i-1] + ins, c[(i+1)/2] + cpy + del);
    }
    return c[n];
} 
int main() { 
	int t; cin>>t;
	while (t--){
        int n,ins,del,cpy; cin>>n>>ins>>del>>cpy;
        cout<<xuly(n,ins,del,cpy);
        cout<<endl;
	}
	return 0; 
} 
