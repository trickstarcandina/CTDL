int a[501][501];
int c[501][501];
void nhap(int n,int m){
    for(int i=0;i<n;i++)
        for(int j=0;j<m;j++)
            cin>>a[i][j];
}
void xuly(int n,int m) { 
	int i,j;  
	//copy hang dau tien va cot dau tien
	for(i = 0; i < n; i++)  c[i][0] = a[i][0]; 
	for(j = 0; j < m; j++)  c[0][j] = a[0][j]; 	
	// add
	for(i = 1; i < n; i++) { 
		for(j = 1; j < m; j++) { 
			if(a[i][j] == 1) //neu == 1 thi 
				c[i][j] = min( c[i][j-1], min ( c[i-1][j], c[i-1][j-1])) + 1; 
			else
				c[i][j] = 0; 
		} 
	}
	int t = c[0][0]; 
	for(i = 0; i < n; i++) { 
		for(j = 0; j < m; j++) { 
			if(t < c[i][j]) { 
				t= c[i][j]; 
			} 
		}			 
	} 
    cout<<t; 
} 
int main() { 
	int t; cin>>t;
	while (t--){
        int n,m; cin>>n>>m;
        nhap(n,m);
        xuly(n,m);
        cout<<endl;
	}
	return 0; 
} 

