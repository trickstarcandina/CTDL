using namespace std;  
/*
"1234"
c[0] = 1 = 1
c[1] = 2 + 12  = 14
c[2] = 3 + 23  + 123 = 149
c[3] = 4 + 34  + 234 + 1234  = 1506
c[3] = 4 + 34 + 234 + 1234
     = 4 + 30 + 4 + 230 + 4 + 1230 + 4
     = 4*4 + 10*(3 + 23 +123)
     = 4*4 + 10*(c[2])
c[i]  =  (i+1)*str[i] + 10*c[i-1]
=> bằng 1670
*/
long long xuly(string str) { 
	int n = str.length(); 
	long long c[n]; //max n=12 
	c[0] = str[0]-'0'; 
	long long result = c[0]; 
	for (int i = 1; i < n; i++) { 
		long long t = str[i]-'0'; 
		// update tổng số từ giá trị trước
		c[i] = (i + 1) * t + 10 * c[i - 1]; 
		//thêm giá trị
		result += c[i]; 
	} 
	return result; 
} 
long long xuly1(string str) { 
	int n = str.length(); 
	long long x = str[0]-'0'; 
	long long result = x; // ans 
	long long c = 0; 
	for (int i = 1; i < n; i++) { 
		int t = str[i]-'0';  
		c = (i + 1) * t + 10 * x; 
		result += c; 
		x = c; 
	} 
	return result; 
} 
