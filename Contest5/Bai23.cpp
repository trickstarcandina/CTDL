void nhap(int n){
    for(int i=0;i<n;i++)
       cin>>a[i];
}
int xuly(int n) { 
    for (int i = 0; i < n; i++) 
        c[i] = 1; 
    //tinh tu duoi len
    for (int i = 1; i < n; i++){
        for (int j = 0; j < i; j++){
            if (a[i] >= a[j] && c[i] < c[j] + 1) 
                c[i] = c[j] + 1;
            //cout<<c[i]<<" ";
        } 
        //cout<<endl;
    } 
    int max = 0; 
    for (int i = 0; i < n; i++) 
        if (max < c[i]) 
            max = c[i];
    return (n - max); 
} 
int main() { 
	int t; cin>>t;
	while (t--){
        int n; cin>>n;
        nhap(n);
        cout << xuly(n); 
        cout<<endl;
	}
	return 0; 
} 
