string Tong(string number1 , string number2){ 
    if(number1.size()<number2.size())
        swap(number1,number2);
    int l1=number1.size(); int l2=number2.size();
    for(int i=1;i<=l1-l2;i++) number2.insert(0,"0");
    int a[l1+1];
    int i,j;
    for (i=l1-1;i>=0;i--) a[i]=(number1[i]-'0')+(number2[i]-'0');
    for (i=l1-1;i>=1;i--)
        if (a[i]>9){
            a[i-1]+=a[i]/10; a[i]%=10;
        }
    string Tong="";
    if(a[0]>9){
        Tong +='0'+1;
        a[0]=a[0]-10;
    }
    for (i=0;i<l1;i++) Tong+='0'+a[i];
    return Tong;
}
string Tich(string number1 , string number2){
    int a[number1.size()+number2.size()];
    int i,j;
    memset(a,0,sizeof(a));
    for (i=number1.size()-1;i>=0;i--)
        for (j=number2.size()-1;j>=0;j--)
            a[i+j+1]+= (number1[i]-'0')*(number2[j]-'0');
    for (i=number1.size()+number2.size()-1;i>=0;i--)
        if (a[i]>9){
            a[i-1]+=a[i]/10;
            a[i]%=10;
        }
    string Tich="";
    if (a[0]!=0)
        for (i=0;i<number1.size()+number2.size();i++) Tich+='0'+a[i];
    else{
        for (i=1;i<number1.size()+number2.size();i++) Tich+='0'+a[i];
    }
    return Tich;
}
