#include <bits/stdc++.h> 
using namespace std; 
int a[1000000];
void nhap(int n){
    for(int i=0;i<n;i++){
        cin>>a[i];
    }       
}
void merge(int l, int m, int r){
    int n1 = m - l + 1;
    int n2 =  r - m;
    int L[n1], R[n2];
    // Copy sang
    for (int i = 0; i < n1; i++) L[i] = a[l + i];
    for (int j = 0; j < n2; j++) R[j] = a[m + 1+ j];
    int i = 0,j = 0; 
    int k = l; // mảng lưu kết quả
    while (i < n1 && j < n2){
        if (L[i] <= R[j]){
            a[k] = L[i]; i++;
        }
        else{
            a[k] = R[j]; j++;
        }
        k++;
    }
    while (i < n1){
        a[k] = L[i]; i++; k++;
    }
    while (j < n2){
        a[k] = R[j]; j++; k++;
    }
}
void mergeSort(int l, int r){
    if (l < r){
        int m = (r+l)/2;
        //đệ quy tiếp tục chia đôi từng nửa mảng
        mergeSort(l, m);
        mergeSort(m+1, r);
        merge(l, m, r);
    }
}
int main() { 
    int t; cin >> t;
    while (t--){
        int n; cin>>n; 
		nhap(n); 
        mergeSort(0, n - 1); 
	    for (int i = 0; i < n; i++) 
		    cout << a[i] << " "; 
        cout<<endl;
    }
    return 0; 
}
