#include <bits/stdc++.h> 
using namespace std; 
int a[1000001];
void nhap(int n){
    for(int i=0;i<n;i++){
        cin>>a[i];
    }       
}
void xuly(int n) { 
    int r = 0, l = n-1, i, j; 
    // tìm từ trái sang phần tử đầu tiên giảm ... r
    for (r = 0; r < n-1; r++) { 
	    if (a[r] > a[r+1]) break; 
    } 
    // tìm từ phải sang phần tử đầu tiên giảm ... l
    for(l = n - 1; l > 0; l--) { 
	    if(a[l] < a[l-1]) break; 
    } 
    // tìm max và min trong đoạn
    int max = a[r];int min = a[r]; 
    for(i = r + 1; i <= l; i++) { 
	    if(a[i] > max) max = a[i]; 
	    if(a[i] < min) min = a[i]; 
    } 
    //tìm phần tử đầu tiên trong đoạn lớn hơn min, đổi s = cái này 
    for(i = 0; i < r; i++) { 
	    if(a[i] > min) { 
	        r = i; 
	        break; 
	    }	 
    } 
    //tìm phần tử đầu tiên trong nhỏ hơn hơn max, đổi l = cái này 
    for(i = n -1; i >= l+1; i--) { 
	    if(a[i] < max) { 
	        l = i; 
	        break; 
	    } 
    } 
    cout << r+1 <<" "<< l+1; // nhập từ 0 nên phải +1 nữa 
} 
int main() { 
	int t; cin>>t;
	while (t--){
        int n; cin>>n;
        nhap(n); 
        xuly(n);
        cout<<endl;
	}
	return 0; 
} 
