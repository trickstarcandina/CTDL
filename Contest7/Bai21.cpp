void timkiem(int ke[],char x){
	stack<int> stk;
    for(int i=n-1;i>=0;i--){
        //int check=0;
        //if(x=='G')
        //    if(a[stk.top()]<=a[i]) check=1;
        //else
        //    if(a[stk.top()]>=a[i]) check=1;  
        
        while(!stk.empty() && ((x=='G')? a[stk.top()] <= a[i]: a[stk.top()] >= a[i]))
            stk.pop();
        if(!stk.empty())
            ke[i]=stk.top();
        else
            ke[i]=-1;
        stk.push(i);
    }
}
void xuly(){
    timkiem(NextGreater,'G');
    timkiem(RightSmaller,'S');
    for(int i=0;i<n;i++){
        if(NextGreater[i]!= -1 && RightSmaller[NextGreater[i]]!=-1)
            cout<<a[RightSmaller[NextGreater[i]]]<<" ";
        else
            cout<<"-1"<<" ";
    }
}
int main(){
	int t; cin>>t;
	while(t--){
        cin>>n;
		nhap();
        xuly();
        cout<<endl;
	}
}
