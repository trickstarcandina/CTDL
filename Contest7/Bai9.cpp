#include <bits/stdc++.h> 
using namespace std; 
string str;
int a[20], b[20], st[20], n, t;
int c1[20], c2[201];
int l;
string res[3000];
int r = 0;
void renew(){
	for (int i = 0; i < l; i++) 
    c2[i] = 1;
	for (int i = 1; i <= n; i++)
	  if (c1[i] == 0){
	  	c2[a[i]] = 0;
	  	c2[b[i]] = 0;
	  }
	r++;
	res[r] = "";
	for (int i = 0; i < l; i++) 
	  if (c2[i]) res[r] += str[i];
}
void tryhard(int i){
	for (int j = 0; j <= 1; j++){
		c1[i] = j;
		if (i == n) renew();
		else tryhard(i + 1);
	}
}
void xuly(){
  n = 0;
	t = 0;
	l = str.length();
	for (int i = 0; i < l; i++)
	  if (str[i] == '('){
	  	t++;
	  	st[t] = i;
	  }
	  else if (str[i] == ')'){
	  	n++;
	  	a[n] = st[t];
	  	b[n] = i;
	  	t--;
	  }
	tryhard(1);
	r--;
	sort(res + 1, res + 1 + r);
	res[0] = "*";
	for (int i = 1; i <= r; i++) 
	  if (res[i] != res[i - 1]) 
      cout << res[i] << endl;
}
int main(){
	cin >> str;
	xuly();
  return 0;
}
