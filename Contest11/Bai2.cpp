#include<bits/stdc++.h>
using namespace std;
int n;
string s[1001];
struct node{
	string value;
	struct node *left;
    struct node *right;
};

int to_int(string a){
	int num=0;
	for(int i=0;i<a.length();i++) 
        num=num*10+(a[i]-'0');
	return num;
}

node *MakeNode(string val){
	node *temp=new node;
	temp->left=temp->right=NULL;
	temp->value=val;
	return temp;
}

node *insertLV(node* root,int i){ 
    if (i < n){ 
        node *temp = MakeNode(s[i]); 
        root = temp; 
        root->left = insertLV(root->left,2*i+1); 
        root->right = insertLV(root->right,2*i+2); 
    } 
    return root; 
}
int proTree(node *root){
	if(!root) 
        return 0;
	if(!root->left && !root->right)  
        return to_int(root->value); 
    
    int l_val=proTree(root->left);   
    int r_val=proTree(root->right); 
	 
    if(root->value=="+")  
        return l_val+r_val;  
  
    if (root->value=="-")  
        return l_val-r_val;  
  
    if (root->value=="*")  
        return l_val*r_val;  
  
    return l_val/r_val; 
} 
