#include<bits/stdc++.h>
using namespace std;
struct node{
    char data;
    struct node * left;
    struct node * right;
};
node* MakeNode(int value){
    node * temp=new node;
    temp->data = value;
    temp->left = temp->right=NULL;
    return temp;
}
bool kiemtra(char c){
    if(c == '+' || c == '-' || c == '*' || c == '/')
        return true;
    return false;
}
void inorder(node *t){
    if(t){
        inorder(t->left);
        cout<<t->data<<" ";
        inorder(t->right);
    }
}
node* proTree(string str){
    stack<node *> stk;
    node *t,*t1,*t2;
    for(int i=0;i<str.length();i++){
        if(!kiemtra(str[i])){
            t = MakeNode(str[i]);
            stk.push(t);
        }
        //nếu là dấu
        else {
            t= MakeNode(str[i]);
            t1=stk.top(); stk.pop();
            t2=stk.top(); stk.pop();
            t->right = t1;
            t->left = t2;
            stk.push(t);
        }
    }
    t =stk.top();
    stk.pop();
    return t;
}
