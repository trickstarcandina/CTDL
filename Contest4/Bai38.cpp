#include <iostream> 
using namespace std;
void nhap(int a[],int m){
    for(int i=0;i<m;i++)
        cin>>a[i];
} 
int xuly(int a1[], int m, int a2[], int n, int k) { 
	if (k > (m+n) || k < 1) return -1; 
	// m <= n 
	if (m > n) return xuly(a2, n, a1, m, k); 	//cay :)) doan nay luc dau ko co bi sai sml
	// neu a1 rong return phan tu thu k cua a2 
	if (m == 0) return a2[k - 1]; 
	// neu k = 1 return tra ve phan tu nho hon cua 1 trong 2 mang
	if (k == 1) return min(a1[0], a2[0]); 
	// chia de tri
	int i = min(m, k / 2), j = min(n, k / 2); 
	if (a1[i - 1] > a2[j - 1] ) // tim phan tu thu k vi da tim ra j nho nhat 
		return xuly(a1, m, a2 + j, n - j, k - j); 
	else//tim phan tu thu k vi da tim ra i nho nhat 
		return xuly(a1 + i, m - i, a2, n, k - i); 
} 
int main() { 
	int t; cin >> t; 
    while (t--) {
        int m,n,k; cin>>m>>n>>k;
        int a1[m], a2[n];
		nhap(a1,m); nhap(a2,n);
		cout<<xuly(a1,m,a2,n,k);
		cout<<endl;
    } 
} 

