#include<bits/stdc++.h> 
using namespace std;
vector <long long> x;
void sinh(){ // sinh do dai day
	long long t = 1; 
    int i = 1; 
    x.push_back(1);
	while(i<=52){
		t *= 2;//x=2 //4 //8
		long long tmp = x[i-1]+t;//=a[0]+2=1+2=3 //a[1]+4=3+4=7 //a[2]+8=7+8=15
		x.push_back(tmp);//3
		i++;
	}
}
void xuly(long long n, long long i, long long leng,long long res,long long & dem){ //dem so bit 1 trong doan
	if(n<=0 || i <=0 || leng<=0) return ;
    //neu r > nua cai ay thi tim ben phai xong lay tong so bit 1 ben trai cong vao bien dem roi lap lai
	if(i >= leng){ //y tuong la: de quy cho n/2 roi dem tiep neu n le thi +1
		dem+=n;
		if(i > leng) dem+= res%2; //truong hop le +1, con chan ko cong
		long long j=i-leng-1;
		xuly(n/2,j,leng/2,n,dem);
	}
	else 
        xuly(n/2,i,leng/2,n,dem);
}
main(){
	sinh();
	int t; cin >> t;
	while(t--){
		long long n,r,l; cin>>n>>l>>r;
		long long leng = 0; 
		for(int i =0 ; i < x.size() ; i++){
			if(x[i] >= n){ //tim do dai day 
				leng = x[i];
				break;
			}
		}
        //so bit 1 trong doan [l,r] = doan [1,r] - doan[1,l-1]
        long long deml=0; xuly(n,l-1,leng,0,deml);
        long long demr=0; xuly(n,r,leng,0,demr);
		cout<< demr-deml <<endl;
	}

}
