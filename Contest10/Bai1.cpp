#include <bits/stdc++.h> 
using namespace std; 
int V,E,m;
bool a[11][11];
void nhap(){
    memset(a,0,sizeof(a));
    cin >> V >> E >>m;
	int x , y;
	for(int i = 0 ; i < E ; i++){
		cin >> x >> y;
		a[x-1][y-1]=1;
        a[y-1][x-1]=1;
	}
}
bool check(int v, int color[], int c) { 
	for (int i = 0; i < V; i++) 
		if ( a[v][i] && c == color[i]) 
			return false; 
	return true; 
} 
bool xuly1( int m, int color[], int v) { 
	if (v == V) 
		return true; 
	for (int c = 1; c <= m; c++) { 
		if (check( v, color, c)) { 
			color[v] = c; 
			if ( xuly1( m, color, v + 1) == true) 
				return true; 
			color[v] = 0; 
		} 
	} 
	return false; 
} 
void xuly2(int m) { 
	int color[V]; 
	for (int i = 0; i < V; i++) 
		color[i] = 0; 
	if ( xuly1( m, color, 0) == false) { 
		cout<<"NO";
        return;
	} 
	cout<<"YES";
} 
int main(){
	int t; cin >> t;
	while(t--){
	    nhap();
        xuly2(m);
        cout<<endl;
	}
    return 0;
}
