#include <bits/stdc++.h>
using namespace std;
typedef pair<int, int> pii;

vector<pii>  dsk[101];

int a[105][105];
int V,E;
void nhap(){
    cin>>V>>E;
	for(int i=0;i<105;i++)
        dsk[i].clear();
	for(int i=1;i<=E;i++){
		int a,b,k;
		cin>>a>>b>>k;
		dsk[a].push_back(pii(b,k));
		dsk[b].push_back(pii(a,k));
	}
}
void xuly(){
	for(int i=1;i<=V;i++){
		for(int j=1;j<=V;j++){
			a[i][j]=INT_MAX;
		}
		priority_queue< pii,vector<pii>,greater<pii> > pq;
		pq.push(pii(0,i));
		a[i][i]=0; 
	
		while(!pq.empty()){
			int u=pq.top().second;
			pq.pop();
			for(int x=0;x<dsk[u].size();x++){
				int v=dsk[u][x].first;
				int temp=dsk[u][x].second;
			
				if(a[i][v]>a[i][u]+temp){
					a[i][v]=a[i][u]+temp;
					pq.push(pii(a[i][v],v));
				}
			}
		}
	}
}
int main(){
    nhap();
	xuly();
	int t; cin>>t;
	while(t--){
		int x,y;
		cin>>x>>y;
		cout<<a[x][y]<<endl;
	}
	return 0;
}
