#include<bits/stdc++.h>
using namespace std;
int d;
vector<int> maxx,a,pri;
int ngto(int x){
    if(x<=1) return 0;
    for(int i=2;i<=sqrt(x);i++){
        if(x%i==0) return 0;
    }
    return 1;
}
void xuat1() { 
	int l = a.size(); d=d+1;
	for (int i = 0; i < l; i++){
		maxx.push_back(a[i]); 
	}	
	maxx.push_back(0);
} 
void xuat2(){
	int p=maxx.size();
	cout<<d<<endl;
	for(int i=0;i<p;i++){
		if(maxx[i]==0) cout<<endl;
		else cout<<maxx[i]<<" ";
	}
}
void prisum(int tong, int n, int s, int kt){  
    int l = a.size();
	if (tong == s && l == n) { 
		xuat1(); 
		return; 
	} 
    int k=pri.size();
	if (tong > s || kt == k) 
		return; 
	a.push_back(pri[kt]); 
	prisum(tong+pri[kt], n, s, kt+1); 
	a.pop_back(); 
	prisum(tong, n, s, kt+1); 
} 
void priall(int n, int p, int s) { 
	for (int i = p; i <=s ; i++) {  
		if (ngto(i)) 
			pri.push_back(i); 
	} 
    int k=pri.size();
	if (k < n) 
		return; 
	prisum(0, n, s, 0); 
} 
int main(){
    int t; cin>>t; 
    while (t--){
		a.clear(); pri.clear(); d=0; maxx.clear(); //reset
        int n,p,s; cin>>n>>p>>s;
		priall(n,p,s);
		xuat2();
    }
    return 0;
}
