#include<bits/stdc++.h>
using namespace std;
int a[1000][1000];
int n;
bool kt = false;
string k[100][100];
int xt[2],yt[2];
void lmss(){
    xt[0]=1; xt[1]=0;
    yt[0]=0; yt[1]=1;
}
void re_new(){
    kt = false;
    for(int i =0 ; i < 100; i++){
        for(int j = 0 ; j < 100 ;j++){
            a[i][j] = 0;
            k[i][j] = "";
        }
    }
}
void nhap(){
    cin >> n;
    for(int i=0;i<n;i++)
        for(int j=0;j<n;j++)
            cin>>a[i][j];
}
void xuly(int x,int y){
    if(a[0][0]==0) return;
    if(x == n-1 && y == n-1){
        kt = true;
        cout << k[x][y] <<" ";
        return;
    }
    else
    for(int i = 0 ; i <= 1 ; i++){
        int m = x+xt[i];
        int l = y+yt[i];
        if(n > m && n > l && a[m][l] == 1 ){
            a[m][l]= 0;
            if(xt[i] == 0 && yt[i] == 1)
                k[m][l]= k[x][y]+'R';
            else
                k[m][l]= k[x][y]+'D';   
            xuly(m,l);
            a[m][l] = 1;
        }
    }
}
int main(){
    lmss();
    int t; cin>>t;
    while(t--){
        re_new();
        nhap();
        xuly(0,0);
        if(kt == false)
            cout << -1;
        cout << endl;
    }
	return 0;
}
