#include <bits/stdc++.h> 
using namespace std; 
int n,k;
int arr[10000];
bool try1(int xsum[], bool np[], int x, int c, int lm) { 
	if (xsum[c] == x) { 
		if (c == k - 2) 
			return true; 
		return try1(xsum, np, x,c + 1, n - 1); 
	} 
	for (int i = lm; i >= 0; i--) { 
		if (np[i]) 
			continue; 
		int temp = xsum[c] + arr[i]; 
		if (temp <= x) { 
			np[i] = true; 
			xsum[c] += arr[i]; 
			bool nxt = try1(xsum, np, x,c, i - 1); 
			np[i] = false; 
			xsum[c] -= arr[i]; 
			if (nxt) 
				return true; 
		} 
	} 
	return false; 
} 
bool try2() { 
	if (k == 1) return true; 
	if (n < k) return false; 
	int sum = 0; 
	for (int i = 0; i < n; i++) 
		sum += arr[i]; 
	if (sum % k != 0) 
		return false; 
	int x= sum / k; 
	int xsum[k]; 
	bool np[n]; 
	for (int i = 0; i < k; i++) 
		xsum[i] = 0; 
	for (int i = 0; i < n; i++) 
		np[i] = false;  
	xsum[0] = arr[n - 1]; 
	np[n - 1] = true; 
	return try1(xsum, np, x, 0, n- 1); 
} 
