#include <bits/stdc++.h> 
using namespace std; 
int k,m,n;
string dic[200];
char a[10][10];
vector<string> res;
void nhap(){
	res.clear();
  cin>>k>>m>>n;
  for(int i=0;i<k;i++){
    cin>>dic[i];
	}
	for(int i=0;i<m;i++){
		for(int j=0;j<n;j++){
			cin>>a[i][j];
		}
	}
}
bool check(string& s) { 
    for (int i = 0; i < k; i++) 
        if (s.compare(dic[i]) == 0) 
            return true; 
    return false; 
} 
void tryhard(bool kt[][10], int i, int j, string s) { 
    kt[i][j] = true; 
    s = s + a[i][j]; 
    if (check(s)) 
        res.push_back(s); 
	  int hang,cot;
    for (hang = i - 1; hang <= i + 1 && 3 > hang; hang++) 
        for (cot = j - 1; cot <= j + 1 && cot < 3; cot++) 
            if (kt[hang][cot]!=1 && hang >= 0 && cot >= 0 ) 
                tryhard(kt, hang, cot, s); 
    s.erase(s.length()-1); 
    kt[i][j] = false; 
} 
int main() { 
    int t; cin>>t;
    while(t--){
    nhap();
		bool kt[10][10] = {false} ; 
    string s=""; 
    for (int i = 0; i < 3; i++) {
		  for (int j = 0; j < 3; j++) 
        tryhard(kt, i, j, s);
    }
		int p=res.size();
		if(p){
			for(int i=0;i<p;i++) 
				cout<<res[i]<<" ";
		}
		else cout<<-1;
		cout<<endl;
	}
    return 0; 
} 
